package model;

public class TuteeObject {
	String tuteeUsername;
	String firstName;
	String lastName;
	String email;
	String sutdentID;
	String major;
	String classYear;
	String secondMajor;
	
	
	public String getSecondMajor() {
		return secondMajor;
	}
	public void setSecondMajor(String secondMajor) {
		this.secondMajor = secondMajor;
	}
	public String getTuteeUsername() {
		return tuteeUsername;
	}
	public void setTuteeUsername(String tuteeUsername) {
		this.tuteeUsername = tuteeUsername;
	}
	public String getFirstName() {
		return firstName;
	}
	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}
	public String getLastName() {
		return lastName;
	}
	public void setLastName(String lastName) {
		this.lastName = lastName;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getSutdentID() {
		return sutdentID;
	}
	public void setSutdentID(String sutdentID) {
		this.sutdentID = sutdentID;
	}
	public String getMajor() {
		return major;
	}
	public void setMajor(String major) {
		this.major = major;
	}
	public String getClassYear() {
		return classYear;
	}
	public void setClassYear(String classYear) {
		this.classYear = classYear;
	}
}
